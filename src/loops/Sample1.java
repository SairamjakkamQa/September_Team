public class Sample1 {

	public static void main(String[] args) {
		
		int range=100;
		
	
		for(int i=1;i<=range;i++) {
			
			if(i%2==0) { // 2
			
				System.out.println("The number is even "+i);				
		}
			
			else
				
				System.out.println("The number is odd "+i);
			
		}}
	}
	
	

